function Checkbox({
  id,
  checked,
  onChange,
  label,
}: {
  id: string;
  checked: boolean;
  onChange: (v: boolean) => void;
  label: string;
}) {
  return (
    <label
      htmlFor={id}
      className="flex items-center gap-2.5 cursor-pointer group"
    >
      <button
        id={id}
        role="checkbox"
        aria-checked={checked}
        onClick={() => onChange(!checked)}
        className={cn(
          "w-4 h-4 rounded border flex items-center justify-center transition-all shrink-0",
          checked
            ? "bg-accent border-accent"
            : "border-border bg-surface-elevated group-hover:border-text-muted",
        )}
      >
        {checked && <Check size={12} className="text-white" strokeWidth={3} />}
      </button>
      <span className="text-sm text-text-secondary">{label}</span>
    </label>
  );
}

export default function ExportReport({
  findings,
  document: doc,
}: {
  findings: Finding[];
  document: DocumentMetadata;
}) {
  const [open, setOpen] = useState(false);
  const [format, setFormat] = useState<ExportFormat>("summary-pdf");
  const [includeResolved, setIncludeResolved] = useState(false);
  const [includeNotes, setIncludeNotes] = useState(true);
  const [includeResolutions, setIncludeResolutions] = useState(true);
  const { addToast } = useToast();

  const handleExport = useCallback(() => {
    const text = generateMemoText(doc, findings, {
      includeResolved,
      includeNotes,
      includeResolutions,
    });

    const blob = new Blob([text], { type: "text/plain;charset=utf-8" });
    const url = URL.createObjectURL(blob);
    const a = window.document.createElement("a");
    a.href = url;
    a.download = `QA-QC-Review-Memo-${new Date().toISOString().slice(0, 10)}.txt`;
    window.document.body.appendChild(a);
    a.click();
    window.document.body.removeChild(a);
    URL.revokeObjectURL(url);

    setOpen(false);
    addToast("Report exported successfully");
  }, [doc, findings, includeResolved, includeNotes, includeResolutions, addToast]);

  const previewFindings = useMemo(() => {
    return includeResolved
      ? findings
      : findings.filter(
          (f) => f.status !== "resolved" && f.status !== "dismissed",
        );
  }, [findings, includeResolved]);

  return (
    <>
      <Dialog.Root open={open} onOpenChange={setOpen}>
        <Dialog.Trigger asChild>
          <button className="flex items-center gap-1.5 px-3 py-1.5 text-sm font-medium text-text-secondary border border-border rounded hover:bg-surface-elevated transition-colors cursor-pointer">
            <Download size={15} />
            Export Report
          </button>
        </Dialog.Trigger>

        <Dialog.Portal>
          <Dialog.Overlay className="fixed inset-0 bg-black/60 backdrop-blur-sm z-50 data-[state=open]:animate-in data-[state=open]:fade-in-0 data-[state=closed]:animate-out data-[state=closed]:fade-out-0" />
          <Dialog.Content className="fixed left-1/2 top-1/2 -translate-x-1/2 -translate-y-1/2 z-50 w-[680px] max-h-[90vh] bg-surface border border-border rounded-lg shadow-2xl flex flex-col data-[state=open]:animate-in data-[state=open]:fade-in-0 data-[state=open]:zoom-in-95 data-[state=closed]:animate-out data-[state=closed]:fade-out-0 data-[state=closed]:zoom-out-95">
            {/* Header */}
            <div className="flex items-center justify-between px-5 py-4 border-b border-border shrink-0">
              <Dialog.Title className="text-base font-semibold text-text-primary">
                Export QA/QC Review Memo
              </Dialog.Title>
              <Dialog.Close className="p-1 rounded hover:bg-surface-elevated text-text-muted hover:text-text-secondary transition-colors cursor-pointer">
                <X size={18} />
              </Dialog.Close>
            </div>

            {/* Body */}
            <div className="flex-1 overflow-y-auto px-5 py-4 space-y-5">
              {/* Format selector */}
              <div>
                <label className="text-xs font-semibold text-text-muted uppercase tracking-wider block mb-2">
                  Export Format
                </label>
                <div className="flex gap-2">
                  {FORMAT_OPTIONS.map((opt) => {
                    const Icon = opt.icon;
                    const active = format === opt.value;
                    return (
                      <button
                        key={opt.value}
                        onClick={() => setFormat(opt.value)}
                        className={cn(
                          "flex items-center gap-2 px-3 py-2 rounded border text-sm font-medium transition-all cursor-pointer",
                          active
                            ? "border-accent bg-accent/10 text-accent"
                            : "border-border bg-surface-elevated text-text-secondary hover:border-text-muted",
                        )}
                      >
                        <Icon size={15} />
                        {opt.label}
                      </button>
                    );
                  })}
                </div>
              </div>

              {/* Options */}
              <div>
                <label className="text-xs font-semibold text-text-muted uppercase tracking-wider block mb-2.5">
                  Options
                </label>
                <div className="space-y-2.5">
                  <Checkbox
                    id="include-resolved"
                    checked={includeResolved}
                    onChange={setIncludeResolved}
                    label="Include resolved findings"
                  />
                  <Checkbox
                    id="include-notes"
                    checked={includeNotes}
                    onChange={setIncludeNotes}
                    label="Include reviewer notes"
                  />
                  <Checkbox
                    id="include-resolutions"
                    checked={includeResolutions}
                    onChange={setIncludeResolutions}
                    label="Include suggested resolutions"
                  />
                </div>
              </div>

              {/* Preview */}
              <div>
                <label className="text-xs font-semibold text-text-muted uppercase tracking-wider block mb-2">
                  Preview
                </label>
                <div className="bg-white rounded-md border border-neutral-200 shadow-inner max-h-[340px] overflow-y-auto px-6 py-5">
                  <MemoPreview
                    doc={doc}
                    findings={previewFindings}
                    includeResolved={includeResolved}
                    includeNotes={includeNotes}
                    includeResolutions={includeResolutions}
                  />
                </div>
              </div>
            </div>

            {/* Footer */}
            <div className="flex items-center justify-between px-5 py-3 border-t border-border shrink-0">
              <p className="text-xs text-text-muted">
                {previewFindings.length} finding
                {previewFindings.length !== 1 && "s"} will be included
              </p>
              <div className="flex items-center gap-2">
                <Dialog.Close className="px-3 py-1.5 text-sm font-medium text-text-secondary border border-border rounded hover:bg-surface-elevated transition-colors cursor-pointer">
                  Cancel
                </Dialog.Close>
                <button
                  onClick={handleExport}
                  className="flex items-center gap-1.5 px-4 py-1.5 text-sm font-semibold text-white bg-accent rounded hover:bg-accent/90 transition-colors cursor-pointer"
                >
                  <Download size={15} />
                  Export
                </button>
              </div>
            </div>
          </Dialog.Content>
        </Dialog.Portal>
      </Dialog.Root>

    </>
  );
}
