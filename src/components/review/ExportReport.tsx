"use client";

import { useState, useMemo, useCallback } from "react";
import * as Dialog from "@radix-ui/react-dialog";
import { Download, X, Check, FileText, Table, FileDown } from "lucide-react";
import { cn } from "@/lib/utils";
import { useToast } from "@/lib/toast-context";
import type { Finding, FindingSeverity, DocumentMetadata } from "@/lib/types";

type ExportFormat = "summary-pdf" | "detailed-pdf" | "csv";

const FORMAT_OPTIONS: {
  value: ExportFormat;
  label: string;
  icon: React.ElementType;
}[] = [
  { value: "summary-pdf", label: "Summary PDF", icon: FileText },
  { value: "detailed-pdf", label: "Detailed PDF", icon: FileDown },
  { value: "csv", label: "CSV Findings Log", icon: Table },
];

const SEVERITY_ORDER: FindingSeverity[] = [
  "critical",
  "major",
  "minor",
  "info",
];
const SEVERITY_LABELS: Record<FindingSeverity, string> = {
  critical: "CRITICAL",
  major: "MAJOR",
  minor: "MINOR",
  info: "INFORMATIONAL",
};
const SEVERITY_HEADER_LINES: Record<FindingSeverity, string> = {
  critical: "─────────────────",
  major: "─────────────",
  minor: "─────────────",
  info: "─────────────────────",
};

function formatDate(date: Date): string {
  return date.toLocaleDateString("en-US", {
    year: "numeric",
    month: "long",
    day: "numeric",
  });
}

function generateMemoText(
  doc: DocumentMetadata,
  findings: Finding[],
  opts: {
    includeResolved: boolean;
    includeNotes: boolean;
    includeResolutions: boolean;
  },
): string {
  const filtered = opts.includeResolved
    ? findings
    : findings.filter((f) => f.status !== "resolved" && f.status !== "dismissed");

  const counts: Record<FindingSeverity, number> = {
    critical: 0,
    major: 0,
    minor: 0,
    info: 0,
  };
  for (const f of filtered) counts[f.severity]++;

  const lines: string[] = [];
  const hr = "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━";

  lines.push("QA/QC REVIEW MEMO");
  lines.push(hr);
  lines.push("");
  lines.push(`Document:    ${doc.documentName}`);
  lines.push(`Prepared by: ${doc.author}`);
  lines.push(`Review Date: ${formatDate(new Date())}`);
  lines.push(`Reviewer:    [AI-Assisted Review — EnviroQA v0.1]`);
  lines.push("");
  lines.push(`QA Readiness Score: ${doc.overallScore}/100`);
  lines.push(`(Measures document completeness, consistency, and citation accuracy)`);
  lines.push("");
  lines.push("");
  lines.push("SUMMARY OF FINDINGS");
  lines.push("━━━━━━━━━━━━━━━━━━");
  lines.push("");
  lines.push(
    `Critical: ${counts.critical} | Major: ${counts.major} | Minor: ${counts.minor} | Informational: ${counts.info}`,
  );
  lines.push("");

  for (const sev of SEVERITY_ORDER) {
    const group = filtered.filter((f) => f.severity === sev);
    if (group.length === 0) continue;

    lines.push("");
    lines.push(`${SEVERITY_LABELS[sev]} FINDINGS`);
    lines.push(SEVERITY_HEADER_LINES[sev]);

    for (const f of group) {
      lines.push("");
      lines.push(`[${f.id}] ${f.title}`);
      if (f.location) lines.push(`Location: ${f.location}`);
      if (f.regulation) lines.push(`Regulation: ${f.regulation}`);
      lines.push(`Status: ${f.status.charAt(0).toUpperCase() + f.status.slice(1)}`);
      lines.push(`Description: ${f.description}`);
      if (opts.includeResolutions && f.suggestedResolution) {
        lines.push(`Recommended Action: ${f.suggestedResolution}`);
      }
      if (opts.includeNotes && f.reviewerNote) {
        lines.push(`Reviewer Note: ${f.reviewerNote}`);
      }
    }
  }

  lines.push("");
  lines.push("");
  lines.push(hr);
  lines.push("END OF REPORT");
  lines.push(
    `Generated by EnviroQA v0.1 on ${formatDate(new Date())}`,
  );

  return lines.join("\n");
}

